# VARIABLES AND DATA TYPES
# Demonstrates variable declaration and data types in Python

# Primitive data types
age = 25
name = "Alice"
height = 5.8
is_active = True

print("Age:", age)
print("Name:", name)
print("Height:", height)
print("Is Active:", is_active)

# Type checking
print("Type of age:", type(age))
print("Type of name:", type(name))

# Type conversion
x = "10"
y = int(x)
print("Converted string to int:", y)

# Multiple assignment
a, b, c = 1, 2, 3
print(f"a={a}, b={b}, c={c}")

# Using f-strings (formatted strings)
print(f"Person: {name}, Age: {age}")
