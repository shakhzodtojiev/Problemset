// VARIABLES AND DATA TYPES
// This demonstrates basic variable declaration and primitive data types in C

#include <stdio.h>

int main() {
    // Integer types
    int age = 25;
    char grade = 'A';
    float height = 5.8;
    double salary = 50000.50;
    
    printf("Age: %d\n", age);
    printf("Grade: %c\n", grade);
    printf("Height: %.1f\n", height);
    printf("Salary: %.2f\n", salary);
    
    return 0;
}
